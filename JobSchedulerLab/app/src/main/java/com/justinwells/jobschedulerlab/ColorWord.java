public class ColorWord {
    private int mColor;
    private String mText;

    public ColorWord(int mColor, String mText) {
        this.mColor = mColor;
        this.mText = mText;
    }

    public int getmColor() {
        return mColor;
    }

    public String getmText() {
        return mText;
    }
}
